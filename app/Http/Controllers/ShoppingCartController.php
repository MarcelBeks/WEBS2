     */
    public function index()
    {
        if(!Session::has('cart')){
            return view('shoppingcart.cart');
        }
        $oldCart = Session::get('cart');
        $cart = new Cart($oldCart);
        return view('shoppingcart.cart', ['products' => $cart->items, 'totalPrice' => $cart->totalPrice]);
        
    }

